function switchCategory(event) {
    const id = event.target.id;

    if (id.includes("design")) {

        moveLogoToTop();
        hideDesignHome();
        hideCodeHome();
        hideCodeProjects();
        showDesignProjects();

    } 
    else if (id.includes("code")){

        moveLogoToTop();
        hideDesignHome();
        hideCodeHome();
        hideDesignProjects();
        showCodeProjects();
 
    }
    else{

        moveLogoToCenter();
        hideCodeProjects();
        hideDesignProjects();
        showDesignHome();
        showCodeHome();

    }

}


