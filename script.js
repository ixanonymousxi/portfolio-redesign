function easyScroll(e) {
    e.preventDefault();
    const href= e.target.hash;
    const offsetTop = document.querySelector(href).offsetTop;

    scroll({
        top: offsetTop,
        behavior: "smooth"
    });
}


function closeLightBox(){

    const imageObjArr = elements["lightBoxImageContainer"].childNodes;
    const imageArr = [];

    for (let i = 0; i < imageObjArr.length; i++){
        if(imageObjArr[i].nodeName !== "#text"){
            imageArr.push(imageObjArr[i]);
        }
    }

    for(let j = 1; j < imageArr.length; j++){
        elements["lightBoxImageContainer"].removeChild(imageArr[j]);
    }


    const circleObjArr = document.querySelector(".circles-container").childNodes;
    const circlesArr = [];

    for (let i = 0; i < circleObjArr.length; i++) {
        if (circleObjArr[i].nodeName !== "#text" && circleObjArr[i].className === "circles") {
            circlesArr.push(circleObjArr[i]);
        }
    }

    for (let j = 1; j < circlesArr.length; j++) {
        document.querySelector(".circles-container").removeChild(circlesArr[j]);
    }

    document.getElementById("1").style.background = "darkgrey";
    document.getElementById("1").click();
    document.querySelector(".circles-container").style.opacity = 0;
    
    elements["lightBox"].style.opacity = "0";
    window.setTimeout(function () { elements["lightBox"].style.display = "none" }, 200);

}

function openLightBox(event){
    const id = event.target.id;

    elements["lightBox"].style.display = "grid";
    window.setTimeout(function(){elements["lightBox"].style.opacity = "1"}, 200);

    elements["lightBoxTitle"].innerHTML = projects[id]["Title"];
    elements["lightBoxCompany"].innerHTML = projects[id]["Company"];
    elements["lightBoxDescription"].innerHTML = projects[id]["Description"];

    elements["lightBoxImage"].src = "Images/designProjects/" + projects[id]["Images"][0];

    if (projects[id]["Images"].length > 1){
        for (let i = 1; i < projects[id]["Images"].length; i++){
            let newImage = document.createElement("img");
            newImage.src = "Images/designProjects/" + projects[id]["Images"][i];
            newImage.id = "img-" + (i + 1);
            elements["lightBoxImageContainer"].appendChild(newImage);


            document.querySelector(".circles-container").style.opacity = 1;

            let newCircle = document.createElement("div");
            newCircle.classList.add("circles");
            newCircle.id = i + 1;
            newCircle.addEventListener("click", imageScroll);
            document.querySelector(".triangle-down-right").before(newCircle);
        }
    }
}



function imageScroll(event){
    const id = event.target.id;
    const img_id = "#img-" + id;
    const offsetTop = id > 1 ? document.querySelector(img_id).offsetTop - 50 : 0;


    const circlesArr = document.querySelectorAll('.circles');
    circlesArr.forEach(circle => circle.style.background = "white");
    document.getElementById(id).style.background = "rgb(91, 192, 190)";

    document.querySelector("#lightboxImgContainer").scroll({
        top: offsetTop,
        behavior: "smooth"
    });

}


function lightBoxArrowRight(){
    const circlesArr = document.querySelectorAll('.circles');

    for(let i=0; i < circlesArr.length; i++){

        let currentCircle = circlesArr[i];
        let lastCircle = circlesArr.length - 1;

        if (i === lastCircle && currentCircle.style.background === "rgb(91, 192, 190)"){
            currentCircle.style.background = "white";
            circlesArr[0].style.background = "rgb(91, 192, 190)";

            circlesArr[0].click();
            break;

        } else if (currentCircle.style.background === "rgb(91, 192, 190)"){
            currentCircle.style.background = "white";
            circlesArr[i + 1].style.background = "rgb(91, 192, 190)";

            circlesArr[i + 1].click();
            break;
        }
    }

}


function lightBoxArrowLeft() {
    const circlesArr = document.querySelectorAll('.circles');
    const circlesArrReversed = [];

    for (let k = circlesArr.length-1; k >= 0; k--){
        circlesArrReversed.push(circlesArr[k]);
    }


    for (let i = 0; i < circlesArrReversed.length; i++) {

        let currentCircle = circlesArrReversed[i];
        let lastCircle = circlesArrReversed.length - 1;

        if (i === lastCircle && currentCircle.style.background === "rgb(91, 192, 190)") {
            currentCircle.style.background = "white";
            circlesArrReversed[0].style.background = "rgb(91, 192, 190)";

            circlesArrReversed[0].click();
            break;

        } else if (currentCircle.style.background === "rgb(91, 192, 190)") {
            currentCircle.style.background = "white";
            circlesArrReversed[i + 1].style.background = "rgb(91, 192, 190)";

            circlesArrReversed[i + 1].click();
            break;
        }
    }

}



function adjustMarginAndHeightOfMain(){
    const heightOfWrapper = elements["wrapper"].clientHeight;
    const widthOfWrapper = elements["wrapper"].clientWidth;
    const heightOfNav = elements["nav"].clientHeight;
    const vh = window.innerHeight;
    

    if(widthOfWrapper > 700){
        document.querySelectorAll(".sides").forEach(div => {
            div.style.minHeight = heightOfWrapper - heightOfNav + "px";
            elements["main"].style.paddingTop = heightOfNav + "px";
        });

        document.querySelectorAll(".div-link").forEach(div => {
            div.style.minHeight = heightOfWrapper - heightOfNav + "px";
        });

    }else{
        document.querySelectorAll(".sides").forEach(div => {
            document.documentElement.style.setProperty('min-height','${window.innerHeight * 0.01}px');
            div.style.minHeight = ((vh - heightOfNav)/2) - 9 + "px";
            elements["main"].style.paddingTop = heightOfNav + "px";
        });
    }

}


